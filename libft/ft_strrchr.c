#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	len;

	len = ft_strlen((char *)s);
	while (1)
	{
		if (s[len] == (char)c)
			return ((char *)(s + len));
		if (len == 0)
			break ;
		len--;
	}
	return (NULL);
}
