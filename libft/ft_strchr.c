#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*dst;

	dst = (char *)s;
	while (1)
	{
		if (*dst == (char)c)
			return (dst);
		if (*dst == '\0')
			break ;
		dst++;
	}
	return (NULL);
}
